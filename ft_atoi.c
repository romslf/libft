#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	i;
	long			res;
	int				sign;

	i = 0;
	res = 0;
	sign = 1;
	while (ft_isws(str[i]))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - 48);
		i++;
	}
	return (res * sign);
}
